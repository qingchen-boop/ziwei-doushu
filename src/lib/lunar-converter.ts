const GAN = ['甲', '乙', '丙', '丁', '戊', '己', '庚', '辛', '壬', '癸'];
const ZHI = ['子', '丑', '寅', '卯', '辰', '巳', '午', '未', '申', '酉', '戌', '亥'];
const SHENGXIAO = ['鼠', '牛', '虎', '兔', '龙', '蛇', '马', '羊', '猴', '鸡', '狗', '猪'];

/** 计算公历日期是当年的第几天 (0-indexed) */
function dayOfYear(y: number, m: number, d: number): number {
  const days = [31, 28, 31, 30, 31, 30, 31, 31, 30, 31, 30, 31];
  if ((y % 4 === 0 && y % 100 !== 0) || y % 400 === 0) days[1] = 29;
  let sum = d - 1;
  for (let i = 0; i < m - 1; i++) sum += days[i];
  return sum;
}

/** 获取某年农历新年的公历日期 */
function getLunarNewYear(y: number): { month: number; day: number } {
  const data = LUNAR_YEARS[y];
  if (!data) return { month: 1, day: 1 };
  const [m, d] = data[2].split('-').map(Number);
  return { month: m, day: d };
}

/** 公历转农历 */
export function solarToLunar(y: number, m: number, d: number): {
  lunarYear: number;
  lunarMonth: number;
  lunarDay: number;
  isLeapMonth: boolean;
  shengxiao: string;
} {
  // 确定农历年
  let lunarYear = y;
  const nyThis = getLunarNewYear(y);
  const nyPrev = getLunarNewYear(y - 1);
  
  // 如果在新年之前，属于上一年
  if (m < nyThis.month || (m === nyThis.month && d < nyThis.day)) {
    lunarYear = y - 1;
  }
  
  const data = LUNAR_YEARS[lunarYear];
  if (!data) {
    const fallbackYear = y - 1900;
    return {
      lunarYear: y,
      lunarMonth: m,
      lunarDay: d,
      isLeapMonth: false,
      shengxiao: SHENGXIAO[(y - 1900) % 12],
    };
  }
  
  const [, monthLengths, , leapMonth] = data;
  const ny = getLunarNewYear(lunarYear);
  
  // 计算从公历年初到当天总天数
  const doySolar = dayOfYear(y, m, d);
  // 计算从公历年初到农历新年总天数
  const doyNewYear = dayOfYear(lunarYear, ny.month, ny.day);
  
  // 距离农历新年的天数
  let daysFromNY = doySolar - doyNewYear;
  
  // 如果为负，说明在新年之前（上一年）
  if (daysFromNY < 0) {
    // 计算上一年总天数来确定
    const prevYearDays = dayOfYear(lunarYear - 1, 12, 31) + 1;
    daysFromNY = prevYearDays - doyNewYear + doySolar;
  }
  
  // 遍历月份，找到对应的农历月
  let remaining = daysFromNY;
  let lunarMonth = 1;
  let isLeapMonth = false;
  
  for (let i = 0; i < monthLengths.length; i++) {
    const monthLen = monthLengths[i] === 1 ? 30 : 29;
    if (remaining < monthLen) {
      lunarMonth = i + 1;
      break;
    }
    remaining -= monthLen;
  }
  
  // 闰月判断
  if (leapMonth > 0 && lunarMonth > leapMonth) {
    // 已过闰月，实际月份减1
    lunarMonth--;
  } else if (leapMonth > 0 && lunarMonth === leapMonth) {
    // 闰月
    isLeapMonth = true;
  }
  
  return {
    lunarYear,
    lunarMonth,
    lunarDay: remaining + 1,
    isLeapMonth,
    shengxiao: SHENGXIAO[(lunarYear - 4) % 12 < 0 ? (lunarYear - 4) % 12 + 12 : (lunarYear - 4) % 12],
  };
}

/** 获取农历年的年干支 */
export function getYearGanZhi(year: number): string {
  const data = LUNAR_YEARS[year];
  if (!data) {
    return GAN[(year - 1984 + 10) % 10] + ZHI[(year - 1984 + 12) % 12];
  }
  const ganIndex = data[0];
  const gan = GAN[ganIndex];
  const zhi = ZHI[(year - 4) % 12 < 0 ? (year - 4) % 12 + 12 : (year - 4) % 12];
  return gan + zhi;
}
